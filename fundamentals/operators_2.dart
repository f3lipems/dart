void main() {
  int a = 10;
  a = a + 10;
  a  += 10; // a = a + 10
  a  -= 10; // a = a - 10
  a  *= 10; // a = a * 10
  a  ~/= 10; // a = a ~/ 10
  a  %= 10; // a = a % 10

  print(a); 

  print(3 > 2); // true
  print(3 < 2); // false
  print(3 >= 2); // true
  print(3 <= 2); // false
  print(3 == 2); // false
  print(3 != 2); // true  
}